n1 = int(input("\nPlease Enter a Number 1: "))
n2 = int(input("Please Enter a Number 2: "))
n3 = int(input("Please Enter a Number 3: "))

l = [n1,n2,n3]

print("\nThe Largest Number is " , max(l))
print("\nThe Smallest Number is " , min(l))